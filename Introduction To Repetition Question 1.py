# Defines the starting number or point of the loop
count = 0

# while count is less than or equal to 25 keep counting (while loop)
while (count <= 25):
# Print - Outputting the count
    print (count)
# Adding 1 to count or the starting point so that it continues rather than looping the starting number over and over
    count = count + 1